package com.training;

public class Calculator {

    public static double addNumbers(double x, double y)
    {
        return x+y;
    }

    public static double substractNumbers(double x, double y)
    {
        return  x-y;
    }

    public static double multiplyNumbers(double x, double y)
    {
        return x*y;
    }

    public static double divideNUmbers(double x, double y)
    {
       return x/y;
    }

    public static double powerOfNumber(double x, double y)
    {
        return Math.pow(x,y);
    }
}
